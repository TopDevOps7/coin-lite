        <h3 class="mt-5">What Are Cookies?</h3>
        <p>
            Cookies are small information files that are stored on your electronic device and that can take on various functions, being able to store the user’s preferences regarding certain types of information or store information related to their website browsing experience, its main objective being the detection of the user in their visits to the website and respective configuration parameters.
            <br>
            The use of cookies on the Internet is normal and in no way harms users’ web access computers and other electronic devices.
        </p>

        <h3 class="mt-5">What Are Cookies Used For?</h3>
        <p>
            Cookies have various functions, such as helping a website’s content management and updating area to understand more deeply how it is used, facilitating their browsing, saving user preferences and, generally speaking, improving their user experience and ensuring that the websites present relevant content to each user.
            Thus, they are instruments of continuous improvement in the browsing experience.
        </p>

        <h3 class="mt-5">What Type Of Do We Use?</h3>
        <p>
            There are several types of cookies which act in different ways and with different purposes regarding your web browsing experience.
        </p>
        <div>
            We present the existing categories we use:
        </div>
        <ul>
            <li>
                <strong>Strictly necessary cookies:</strong>
                these essential cookies guarantee website navigation and use of its functionalities, including registration of whether or not the user has authorised the use of cookies on the website.
                These cookies cannot be rejected when using a website.
            </li>
            <li>
                <strong>Functional cookies:</strong>
                these cookies allow websites to remember information about user choices, such as username, language, region from which they access and user customization of the website browsing experience.
                These cookies lose their validity at the end of the user’s session (for example, when the user closes the browser window) or may remain for a longer period to store the user’s website preferences and choices.
            </li>
            <li>
                <strong>Advertising cookies:</strong>
                these cookies collect information on the user’s browsing habits by registering website uses.
                These cookies are used to support the performance of more targeted marketing and communication actions, allowing the distinction and identification of individual usage preferences or storage of a code that translates a set of usage habits or preferences.
            </li>
            <li>
                <strong>Analytical cookies:</strong>
                these cookies are used to analyse how users use the website and to monitor the website’s performance, for example, which pages have the highest volume of visits or if there are any error message returned to the user.
                This makes it possible to provide a high-quality experience by customizing the offering and quickly identifying and fixing any issues that may arise.
                These cookies are used for continuous improvement and statistical analysis.
            </li>
        </ul>

